package com.robot.bazoo.data;

public class PointNumber {

	//点数  1、2、3、4、5、6
	private int point;
	//某个点数的数量
	private int number;
	
	
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	
	
}
