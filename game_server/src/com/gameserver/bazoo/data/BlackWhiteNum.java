package com.gameserver.bazoo.data;
/**
 * 红黑单双的数量
 * @author JavaServer
 *
 */
public class BlackWhiteNum {

	private int red;
	private int black;
	private int single;
	private int doubles;
	public int getRed() {
		return red;
	}
	public void setRed(int red) {
		this.red = red;
	}
	public int getBlack() {
		return black;
	}
	public void setBlack(int black) {
		this.black = black;
	}
	public int getSingle() {
		return single;
	}
	public void setSingle(int single) {
		this.single = single;
	}
	public int getDoubles() {
		return doubles;
	}
	public void setDoubles(int doubles) {
		this.doubles = doubles;
	}
	
	
	
	
	
}
